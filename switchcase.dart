void main(){
  var dayOfWeek = 5;
  switch(dayOfWeek) {
    case 1:
    print("Day is Sunday.");
    break;
    case 2:
    print("Day is Monday.");
    break;
    case 3:
    print("Day is Tuesday.");
    break;
    case 4:
    print("Day is Wednesday.");
    break;
    case 5:
    print("Day is Thursday.");
    break;
    default:
      print("Invalid weekday");
    break;
  }
}